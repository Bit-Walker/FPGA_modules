`timescale 1ns / 1ps

module mux #(
    //================================================================
    // 可选参数
    //================================================================

    // 输入路数，范围 2 ~ 255
    parameter [7:0] INPUT_COUNT = 4,
    // 数据位宽，单位为 bit
    parameter [7:0] DATA_WIDTH  = 8,

    //================================================================
    // 自动计算参数
    //================================================================

    // 选择信号位宽
    parameter [7:0] SEL_WIDTH = clog2(INPUT_COUNT)
) (
    //================================================================
    // 模块输入
    //================================================================

    // 多路输入数据，按 {in[N-1], ..., in[1], in[0]} 排列
    input  wire [INPUT_COUNT * DATA_WIDTH - 1:0] data_in,
    // 二进制编码选择信号
    input  wire [SEL_WIDTH - 1:0] sel,

    //================================================================
    // 模块输出
    //================================================================

    // 选中的输出数据
    output reg  [DATA_WIDTH - 1:0] data_out
);

    //================================================================
    // 函数声明
    //================================================================

    // 计算 log2 函数
    function integer clog2;
        input [7:0] value;
        reg   [7:0] value_minus_one;
        begin
            value_minus_one = value - 1'b1;
            for (clog2 = 0; value_minus_one > 0; clog2 = clog2 + 1) begin
                value_minus_one = value_minus_one >> 1;
            end
        end
    endfunction

    //================================================================
    // 组合逻辑
    //================================================================

    always @(*) begin
        if (sel < INPUT_COUNT) begin
            data_out = data_in[sel * DATA_WIDTH +: DATA_WIDTH];
        end else begin
            data_out = {DATA_WIDTH{1'b0}};
        end
    end

endmodule
