`timescale 1ns / 1ps

module bcd_to_bin #(
    //================================================================
    // 可选参数
    //================================================================

    // BCD 位数，每位对应一个十进制数字
    parameter [7:0] DIGITS    = 8,
    // 二进制输出位宽
    parameter [7:0] BIN_WIDTH = 32

) (
    //================================================================
    // 模块输入
    //================================================================

    // BCD 输入，高位数字在高位 bit
    input  wire [DIGITS * 4 - 1:0] bcd_value,

    //================================================================
    // 模块输出
    //================================================================

    // 转换后的二进制值
    output reg  [BIN_WIDTH - 1:0] bin_value
);

    //================================================================
    // 整数声明
    //================================================================

    integer digit_index;

    //================================================================
    // 组合逻辑
    //================================================================

    always @(*) begin
        bin_value = {BIN_WIDTH{1'b0}};

        for (digit_index = 0; digit_index < DIGITS; digit_index = digit_index + 1) begin
            bin_value = (bin_value * 4'd10) + bcd_value[((DIGITS - digit_index) * 4) - 1 -: 4];
        end
    end

endmodule
