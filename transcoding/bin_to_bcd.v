`timescale 1ns / 1ps

//================================================================
// 模块说明
//================================================================
// 二进制转 BCD 码转换器。
// 将二进制数值转换为 BCD 编码的十进制表示。
// 采用移位加三算法：
// 从二进制最高位开始逐位移入 BCD 寄存器，
// 每次移位前对大于等于 5 的 BCD 位加 3 进行修正，保证移位后该位不会大于等于十。
//================================================================

module bin_to_bcd #(
    //================================================================
    // 可选参数
    //================================================================

    // 二进制输入位宽，范围 1 ~ 64，单位为 bit
    parameter [7:0] BIN_WIDTH = 32,
    // BCD 输出位数，范围 1 ~ 20，单位为 digit
    parameter [7:0] BCD_DIGITS = 10

) (
    //================================================================
    // 模块输入
    //================================================================

    // 待转换的二进制数值
    input  wire [BIN_WIDTH - 1:0] bin_value,

    //================================================================
    // 模块输出
    //================================================================

    // BCD 编码结果，每 4 bit 表示 1 位十进制数码
    output reg  [BCD_DIGITS * 4 - 1:0] bcd_value
);

    //================================================================
    // 整数声明
    //================================================================

    integer digit_index;
    integer bit_index;

    //================================================================
    // 组合逻辑
    //================================================================

    always @(*) begin
        bcd_value = {(BCD_DIGITS * 4){1'b0}};

        for (bit_index = BIN_WIDTH; bit_index > 0; bit_index = bit_index - 1) begin
            // 移位前，对大于等于 5 的 BCD 位加 3
            for (digit_index = 0; digit_index < BCD_DIGITS; digit_index = digit_index + 1) begin
                if (bcd_value[digit_index * 4 +: 4] >= 4'd5) begin
                    bcd_value[digit_index * 4 +: 4] = bcd_value[digit_index * 4 +: 4] + 4'd3;
                end
            end

            // 将下一位二进制输入移入 BCD 结果
            bcd_value = {bcd_value[BCD_DIGITS * 4 - 2:0], bin_value[bit_index - 1]};
        end
    end

endmodule
